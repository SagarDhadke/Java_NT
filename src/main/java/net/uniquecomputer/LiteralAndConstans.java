package net.uniquecomputer;

public class LiteralAndConstans {
    public static void main(String[] args) {

        //Literals
        //CONSTANTS (Fixed Value), App Name, Phone Number, Address.
        // use "final" keyword  'final means ki fix value you can't change it.'

       final String PHONE_NUMBER = "+91 8828088280";
        System.out.println(PHONE_NUMBER);

        final String APP_NAME = "Unique Computer";
        System.out.println(APP_NAME);

        final int PIN_CODE = 400051;
        System.out.println(PIN_CODE);




    }
}
